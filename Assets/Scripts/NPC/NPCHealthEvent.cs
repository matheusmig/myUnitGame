//////////////////////////////////////////////////////////////////////////////
// NPCHealthEvent
//////////////////////////////////////////////////////////////////////////////
// This event is dispatched when the health player status is changed.
//////////////////////////////////////////////////////////////////////////////

//default namespaces
using UnityEngine;
using System.Collections;

//Add access to game events classes
using GameEvents;

//NPCHealthEvent is a GameEvent
public class NPCHealthEvent :  GameEvent {

    ///////////////////////////////////////////////////////////////////////////
    // EVENT DATA
    ///////////////////////////////////////////////////////////////////////////
	public int   NPCInstanceID;
    public float NPCHealth;

    ///////////////////////////////////////////////////////////////////////////
    // CONSTRUCTOR
    ///////////////////////////////////////////////////////////////////////////

    public NPCHealthEvent(int id, float health) {
		NPCInstanceID = id;
        NPCHealth = health;
    }
}