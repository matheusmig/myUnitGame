//////////////////////////////////////////////////////////////////////////////
// PlayerMoveEvent
//////////////////////////////////////////////////////////////////////////////
// This event is dispatched when a movement key is pressed.
//////////////////////////////////////////////////////////////////////////////

//default namespaces
using UnityEngine;
using System.Collections;

//Add access to game events classes
using GameEvents;

//PlayerMoveEvent is a GameEvent
public class PlayerMoveEvent : GameEvent
{
	
	///////////////////////////////////////////////////////////////////////////
	// EVENT DATA
	///////////////////////////////////////////////////////////////////////////
	
	public Vector3 direction;
	public KeyCode pressedKey;
	
	///////////////////////////////////////////////////////////////////////////
	// CONSTRUCTOR
	///////////////////////////////////////////////////////////////////////////
	
	public PlayerMoveEvent(Vector3 d, KeyCode k)
	{
		direction = d;
		pressedKey = k; 
	}

	public PlayerMoveEvent()
	{
		direction = Vector3.zero;
	}
	
}